package io.codelex.arithmetic.practice;

public class Exercise4 {
    private static void Product1ToN() {
        int factorial = 1;
        for (int i = factorial; i <= 10; i++) {
            factorial *= i;
        }
        System.out.println(factorial);
    }

    public static void main(String[] args) {
        Product1ToN();
    }
}
